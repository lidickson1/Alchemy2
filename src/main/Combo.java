package main;

public class Combo {

    private String element;
    private String a;
    private String b;

    public Combo(String element, String a, String b) {
        this.element = element;
        this.a = a;
        this.b = b;
    }

    public String getElement() {
        return this.element;
    }

    public String getA() {
        return this.a;
    }

    public String getB() {
        return this.b;
    }
}
